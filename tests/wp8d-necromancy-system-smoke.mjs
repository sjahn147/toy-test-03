import assert from 'node:assert/strict';
import { HeroNecromancySystem } from '../src/sim/heroes/HeroNecromancySystem.js';

const aldren={id:'aldren',heroId:'hero.aldren',name:'Aldren',role:'hero-aldren',ecologyFaction:'undead-host',roomId:'E22',roomCell:{x:0,z:0},alive:true,hp:100,maxHp:100};
const malcor={id:'malcor',heroId:'hero.malcor',name:'Malcor',role:'hero-malcor',ecologyFaction:'undead-host',roomId:'E24',roomCell:{x:0,z:0},alive:true,hp:45,maxHp:96,attack:12};
const enemy={id:'enemy',name:'Cleric',role:'cleric',faction:'party',roomId:'E24',roomCell:{x:0,z:2},alive:true,hp:50,maxHp:50,attack:8};
const corpses=[{id:'corpse-a',roomId:'E22',role:'fighter'},{id:'corpse-b',roomId:'E24',role:'cleric'},{id:'corpse-c',roomId:'E24',role:'fighter'},{id:'corpse-d',roomId:'E24',role:'rogue'}];
const effects=[];
const sim={agents:[aldren,malcor,enemy],ecosystem:{corpses},combatSystem:{initializeAgent(){}},emitEffect:(type,data)=>effects.push({type,...data})};
const system=new HeroNecromancySystem();
const royal=system.reassembleRoyalSkeletons(aldren,{maximum:2,duration:10},sim);
assert.ok(royal.length>=1);
assert.equal(royal[0].heroSummonKind,'royal-skeleton');
const memory=system.consumeMemoryCorpse(malcor,{heal:24,buffDuration:8},sim);
assert.equal(memory.role,'cleric');
assert.equal(malcor.hp,69);
assert.ok(malcor.heroStatModifiers.memoryFlesh);
malcor.heroFacing={x:0,z:1};
assert.equal(system.fearCone(malcor,{length:5,width:4,fearDuration:4,paralyzeDuration:1.2},sim),1);
assert.ok(enemy.heroStatuses.heroParalysis);
const ghouls=system.raiseGhoulPack(malcor,{maximum:2,duration:12},sim);
assert.ok(ghouls.length>=1);
assert.equal(ghouls[0].heroSummonKind,'ghoul');
system.applyGhoulFrenzy(malcor,{duration:5,attackMultiplier:1.2,speedMultiplier:1.1},sim);
assert.ok(ghouls[0].heroStatuses.ghoulFrenzy);
system.update(0.2,sim);
assert.ok(enemy.heroStatuses.ghastStench);
assert.ok(effects.some(e=>e.type==='hero-summon-emerge'));
assert.doesNotThrow(()=>JSON.stringify(system.snapshot()));
console.log('WP8-D necromancy system smoke passed');
