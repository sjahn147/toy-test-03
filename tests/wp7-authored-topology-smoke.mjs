import assert from 'node:assert/strict';
import { readFile } from 'node:fs/promises';
import { getAuthoredCampaignLayout } from '../src/content/layout/AuthoredCampaignLayout.js';
import { buildDungeonTopology } from '../src/engine/DungeonTopology.js';
import { VerticalConnectorTopology } from '../src/domain/VerticalConnectorTopology.js';
const manifest=JSON.parse(await readFile(new URL('./sleeping-citadel-manifest-min.json',import.meta.url),'utf8'));
const layout=getAuthoredCampaignLayout(manifest);
const rooms=manifest.rooms.map(room=>({id:room.id,w:room.size[0],d:room.size[1],floorId:room.floorId,...layout.rooms[room.id]}));
const topology=buildDungeonTopology(rooms,layout.routes,{includeInactive:true});
assert.equal(topology.connections.length,90);assert.ok(topology.connections.every(route=>route.fromFloor===route.toFloor&&!route.vertical));
const connectors=new VerticalConnectorTopology(layout.verticalConnectors,rooms);
assert.equal(connectors.all().length,9);assert.equal(connectors.get('route-A05-B06').id,'VC-01');assert.equal(connectors.findBetween('A05','B06').kind,'vertical-connector');
console.log(JSON.stringify({horizontalConnections:topology.connections.length,verticalConnectors:connectors.all().length},null,2));
